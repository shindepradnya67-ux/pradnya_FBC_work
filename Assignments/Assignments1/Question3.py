## Program to find quotient and Remainder of two number

## Algorithm 

# 1.Start.
# 2.Input two numbers:
# 3.Dividend (A)
# 4.Divisor (B)
# 5.Calculate Quotient:
# 6.Quotient = A // B
# 7.Calculate Remainder:
# 8.Remainder = A % B
# 9.Display the Quotient and Remainder.
# 10.Stop.

## Program :

dividend = int(input("Enter Dividend : "))
divisor = int(input("Enter Divisor : "))

Quotient = dividend // divisor

Remainder = dividend % divisor

print(f'Quotient is {Quotient} and Remainder is {Remainder}.')

