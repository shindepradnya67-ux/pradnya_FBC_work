## Write a program to calculate area of rectangle . 

## Algorithm :-
# 1.Start
# 2.Input Length (A) and Breadth (B)
# 3.Calculate Area = L*B
# 4.Display Area
# 5.Stop

## Program :-

Length = float(input("Enter Length : "))
Breadth = float(input("Enter Breadth : "))

Area = Length*Breadth

print("Area of Reactangle ", Area)

